"""
Unified error codes for the ERP system.
"""


class ErrorCodes:
    # General (40000-40099)
    BAD_REQUEST = 40000
    VALIDATION_ERROR = 40001
    NOT_FOUND = 40004

    # Auth (40100-40399)
    UNAUTHORIZED = 40100
    TOKEN_EXPIRED = 40101
    PERMISSION_DENIED = 40300

    # Business (41000-41099)
    INSUFFICIENT_STOCK = 41001
    DUPLICATE_ORDER_NO = 41002
    ORDER_ALREADY_APPROVED = 41003
    CANNOT_MODIFY_APPROVED = 41004
    INVALID_STATUS_TRANSITION = 41005

    # Inventory (41100-41199)
    STOCK_LOCK_FAILED = 41101
    NEGATIVE_STOCK_NOT_ALLOWED = 41102
